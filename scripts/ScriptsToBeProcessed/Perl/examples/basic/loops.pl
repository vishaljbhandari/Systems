use strict;
use warnings;

main(@ARGV);

sub main
{
    open(FH, "linesfile.txt");
    message("the file is open");
    close FH;
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}

